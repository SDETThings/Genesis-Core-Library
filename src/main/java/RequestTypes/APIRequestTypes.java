/**
 * APIRequestTypes class contains an enumeration of type of API methods
 */
public class APIRequestTypes {
    /**
     * RequestTypes is enumeration which lists down values for all API method types
     */
    public enum RequestTypes {
        GET,
        POST,
        PUT,
        PATCH,
        DELETE,
        HEAD,
        OPTIONS,
        CONNECT,
        TRACE
    }
}
